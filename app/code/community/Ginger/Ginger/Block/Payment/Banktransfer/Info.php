class Ginger_Ginger_Block_Payment_Banktransfer_Info extends Mage_Payment_Block_Info
{
    protected $_mailingAddress;

    protected function _construct()
    {
        parent::_construct();
        $this->setTemplate('ginger/info/banktransfer.phtml');
    }

    /**
     *
     * @return string
     */
    public function getMailingAddress()
    {
        if (is_null($this->_mailingAddress)) {
            $this->_convertAdditionalData();
        }
        return $this->_mailingAddress;
    }

    /**
     * @return Ginger_Ginger_Block_Payment_Banktransfer_Info
     */
    protected function _convertAdditionalData()
    {
        $details = @unserialize($this->getInfo()->getAdditionalData());
        if (is_array($details)) {
            $this->_mailingAddress = isset($details['mailing_address']) ? (string) $details['mailing_address'] : '';
        } else {
            $this->_mailingAddress = '';
        }

        return $this;
    }
}
